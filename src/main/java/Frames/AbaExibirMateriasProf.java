    private void configurarTabela(){
        // Criar um modelo de tabela vazio com as colunas desejadas
        String[] colunas = {"Nome", "Matéria"};
        DefaultTableModel modeloTabela = new DefaultTableModel(colunas, 0);

        // Preencher o modelo de tabela com os dados dos professores da série desejada
        for (Professor professor : lista) {
            String nomeProfessor = professor.getNome();
            Map<String, String[]> serieMaterias = professor.getSerieMaterias();

            if (serieMaterias.containsKey(serie)) {
                String[] materias = serieMaterias.get(serie);

                for (String materia : materias) {
                    Object[] rowData = {nomeProfessor, materia};
                    modeloTabela.addRow(rowData);
                }
            }
        }

        // Definir o modelo de tabela na JTable
        jTable1.setModel(modeloTabela);
    
    }
    
